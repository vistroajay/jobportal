package com.vistro.india.bo;

public class Certification {
	private String CertificationName;
	private String Fromcertification;
	private String Tocertification;
	private String EmailID;
	public String getCertificationName() {
		return CertificationName;
	}
	public void setCertificationName(String certificationName) {
		CertificationName = certificationName;
	}
	
	public String getFromcertification() {
		return Fromcertification;
	}
	public void setFromcertification(String fromcertification) {
		Fromcertification = fromcertification;
	}
	public String getTocertification() {
		return Tocertification;
	}
	public void setTocertification(String tocertification) {
		Tocertification = tocertification;
	}
	public String getEmailID() {
		return EmailID;
	}
	public void setEmailID(String emailID) {
		EmailID = emailID;
	}



}
